export function generateMarkdownReport(session, events) {
  const timestamp = new Date().toLocaleString();
  let md = `# Laboratory Report: ${session.name}\n\n`;
  md += `**Date of Execution:** ${timestamp}\n`;
  md += `**System Environment:** Helm's Paladin CTF Assistant\n\n`;
  
  md += `## Table of Contents\n`;
  md += `1. [Overview](#1-overview)\n`;
  md += `2. [Reconnaissance Observations](#2-reconnaissance-observations)\n`;
  md += `3. [Operation Timeline](#3-operation-timeline)\n`;
  md += `4. [Technical Evidence](#4-technical-evidence)\n`;
  md += `5. [Conclusion](#5-conclusion)\n\n`;

  md += `## 1. Overview\n`;
  md += `This document serves as the official laboratory report for the reconnaissance session conducted on **${session.name}**. It contains a detailed log of commands executed, observations recorded, and technical evidence captured during the engagement.\n\n`;

  // 2. Observations (Notes)
  const notes = events.filter(e => e.type === 'note');
  md += `## 2. Reconnaissance Observations\n`;
  if (notes.length > 0) {
    notes.forEach(note => {
      md += `*   **Observation:** ${note.content} [Timestamp: ${new Date(note.timestamp).toLocaleTimeString()}]\n`;
    });
  } else {
    md += `No manual observations were recorded during this session.\n`;
  }
  md += `\n`;

  // 3. Operation Timeline (Commands)
  const commands = events.filter(e => e.type === 'command');
  md += `## 3. Operation Timeline\n`;
  if (commands.length > 0) {
    commands.forEach(cmd => {
      md += `### 3.${commands.indexOf(cmd) + 1}. Execution: ${cmd.command}\n`;
      md += `*   **Status:** ${cmd.status.toUpperCase()}\n`;
      if (cmd.output) {
        const cleanOutput = cmd.output.length > 1200 ? cmd.output.substring(0, 1200) + '... [Full output truncated for brevity]' : cmd.output;
        md += `\n**Console Output:**\n\`\`\`text\n${cleanOutput}\n\`\`\`\n`;
      }
      md += `\n`;
    });
  } else {
    md += `No command executions were logged.\n`;
  }

  // 4. Technical Evidence (Screenshots)
  const screenshots = events.filter(e => e.type === 'screenshot');
  md += `## 4. Technical Evidence\n`;
  if (screenshots.length > 0) {
    screenshots.forEach(ss => {
      md += `### 4.${screenshots.indexOf(ss) + 1}. ${ss.name || 'Screenshot Data'}\n`;
      md += `![${ss.name}](/api/media/${session.id}/${ss.filename})\n`;
      if (ss.tag) md += `*   **Classification Tag:** #${ss.tag}\n`;
      md += `\n`;
    });
  } else {
    md += `No visual evidence (screenshots) was captured during the session.\n`;
  }

  md += `## 5. Conclusion\n`;
  md += `The reconnaissance session for **${session.name}** has been concluded. All recorded activities and data points presented in this report constitute the final findings of the laboratory exercise.\n\n`;
  
  md += `---\n*Laboratory Report generated by Helm's Paladin*`;
  return md;
}
