export function generateMarkdownReport(session, events) {
  const timestamp = new Date().toLocaleString();
  let md = `# Write-up: ${session.name}\n`;
  md += `**Date:** ${timestamp}\n`;
  md += `**Assistant:** Helm's Paladin 🛡️\n\n`;
  md += `## Overview\nThis report was autogenerated based on the reconnaissance timeline for the session **${session.name}**.\n\n`;

  // 1. Discoveries (Notes tagged or just all notes)
  const notes = events.filter(e => e.type === 'note');
  if (notes.length > 0) {
    md += `## 💡 Key Discoveries & Notes\n`;
    notes.forEach(note => {
      md += `- ${note.content} *(${new Date(note.timestamp).toLocaleTimeString()})*\n`;
    });
    md += `\n`;
  }

  // 2. Timeline of Operations (Commands)
  const commands = events.filter(e => e.type === 'command');
  if (commands.length > 0) {
    md += `## 🛠️ Operations Log\n`;
    commands.forEach(cmd => {
      md += `### ${cmd.command}\n`;
      md += `**Status:** ${cmd.status.toUpperCase()}\n`;
      if (cmd.output) {
        // Limit output length for the report
        const cleanOutput = cmd.output.length > 1000 ? cmd.output.substring(0, 1000) + '... [output truncated]' : cmd.output;
        md += `\`\`\`text\n${cleanOutput}\n\`\`\`\n`;
      }
      md += `\n`;
    });
  }

  // 3. Evidence (Screenshots)
  const screenshots = events.filter(e => e.type === 'screenshot');
  if (screenshots.length > 0) {
    md += `## 📸 Evidence & Screenshots\n`;
    screenshots.forEach(ss => {
      md += `### ${ss.name || 'Screenshot'}\n`;
      // In a real scenario, these would be embedded if the report is viewed in the app
      md += `![${ss.name}](/api/media/${session.id}/${ss.filename})\n`;
      if (ss.tag) md += `**Tag:** #${ss.tag}\n`;
      md += `\n`;
    });
  }

  md += `\n---\n*Generated by Helm's Paladin*`;
  return md;
}
